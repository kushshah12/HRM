package VO;

public class QualificationVO {

	public int groupid;
	public int companyid;
	public int qualificationid;
	public String qualificationdes;
	public String createdBy;
	public String UpdatedBy;
	
	public int getGroupid() {
		return groupid;
	}
	public void setGroupid(int groupid) {
		this.groupid = groupid;
	}
	public int getCompanyid() {
		return companyid;
	}
	public void setCompanyid(int companyid) {
		this.companyid = companyid;
	}
	public int getQualificationid() {
		return qualificationid;
	}
	public void setQualificationid(int qualificationid) {
		this.qualificationid = qualificationid;
	}
	public String getQualificationdes() {
		return qualificationdes;
	}
	public void setQualificationdes(String qualificationdes) {
		this.qualificationdes = qualificationdes;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getUpdatedBy() {
		return UpdatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		UpdatedBy = updatedBy;
	}
	
	
}
